# =====================================================
# ------ FUNCTION FOR VIVADO EXECUTION ------
# =====================================================
def execute_vivado(bat_file_path: str, working_dir: str):
    start_time = time.time()

    try:
        result = subprocess.run(
            [bat_file_path],
            cwd=working_dir,
            capture_output=True,
            text=True,
            shell=True,
            timeout=300
        )

        raw_output = result.stdout + "\n" + result.stderr
        returncode = result.returncode

    except subprocess.TimeoutExpired:
        print("VIVADO timeout")
        raw_output = "ERROR: Timeout - Vivado simulation or compilation hanged."
        returncode = 1

    end_time = time.time()
    exec_time = round(end_time - start_time, 2)

    return returncode, raw_output, exec_time


# =====================================================
# ------ FUNCTION FOR SAVING CHECKER METRICS ------
# =====================================================
def save_checker_metrics(
    state: AgentState,
    status: Status,
    exec_time: float,
    coverage_val: str,
    error_summary: str,
    raw_errors: str
):
    """
    Save Vivado/checker experimental data in TXT and CSV files.
    This file is separate from agent_metrics.csv.
    """

    results_dir = os.path.join("..", "results")
    os.makedirs(results_dir, exist_ok=True)

    raport_path = os.path.join(results_dir, "raport_experimental.txt")
    csv_path = os.path.join(results_dir, "experimental_metrics_FIFO3.csv")

    timestamp = datetime.datetime.now().strftime("%Y-%m-%d %H:%M:%S")
    generated_solutions = state.get("iterations", 0)
    tokens = state.get("iteration_tokens", 0)

    # Save to TXT
    content_txt = (
        f"[{timestamp}] "
        f"GeneratedSolutions: {generated_solutions} | "
        f"Status: {status.value} | "
        f"Coverage: {coverage_val} | "
        f"Vivado_exec_time: {exec_time}s\n"
    )

    if status == Status.FAILED:
        content_txt += f"Errors:\n{raw_errors}\n"

    save_to_file(content_txt, raport_path)

    # Save to CSV
    checker_header = [
        "Timestamp",
        "GeneratedSolutions",
        "Status",
        "Vivado_Execution_sec",
        "Coverage",
        "Error_type",
        "Tokens_Used"
    ]

    content_csv = [
        timestamp,
        generated_solutions,
        status.value,
        exec_time,
        coverage_val,
        error_summary,
        tokens
    ]

    save_to_csv(content_csv, csv_path, header=checker_header)


