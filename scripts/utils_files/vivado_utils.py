# Utility functions for running Vivado/XSim and analyzing simulation errors.

def execute_vivado(bat_file_path: str, working_dir: str):
    """Executes the Vivado/XSim run script and returns its output and execution time."""
    start_time = time.time()
    # Vivado/XSim execution.
    try:
        result = subprocess.run(
            [bat_file_path],
            cwd=working_dir,
            capture_output=True,
            text=True,
            shell=True,
            timeout=300
        )

        raw_output = result.stdout + "\n" + result.stderr
        returncode = result.returncode

    except subprocess.TimeoutExpired:
        print("VIVADO timeout")
        raw_output = "ERROR: Timeout - Vivado simulation or compilation hanged."
        returncode = 1

    end_time = time.time()
    exec_time = round(end_time - start_time, 2)

    return returncode, raw_output, exec_time


